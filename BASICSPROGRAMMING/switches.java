package BASICSPROGRAMMING;
public class switches {
    public static void main(String[] args) {
        String day = "Sunday";

        switch (day) {
            case "Sunday":
                System.out.println("it is Sunday");
                break;

            case "Momday":
                System.out.println("it is Monday");
                break;

            default:
                break;
        }
    }
}
